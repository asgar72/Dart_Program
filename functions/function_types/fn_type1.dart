/* Function With No Parameter And No Return Type -->
In this function, you do not pass any parameter and expect no return type. 
Here is an example of it:*/

void main() {
  printName();
}

void printName() {
  print("My name is Asgar abbas.");
}

/*
In this program, printName() is the function which has keyword void. 
It means it has no return type, and the empty pair of parentheses implies that there is no parameter that is passed to the function.
*/
