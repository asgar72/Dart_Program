/* Function With Parameter And No Return Type -->
In this function, you do pass the parameter and expect no return type. 
Here is an example of it:
*/

void main() {
  printName("Abbas");
}

void printName(String name) {
  print("Welcome, ${name}.");
}

/*
In this program, printName(String name) is the function which has keyword void.
It means it has no return type, and the pair of parentheses is not empty but this time that suggests it to accept an parameter.
*/