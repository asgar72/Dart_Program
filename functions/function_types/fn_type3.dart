/* Function With No Parameter And Return Type -->
In this function, you do not pass any parameter but expect return type. 
Here is an example of it:
*/

void main() {
  String name = primeMinisterName();
  print("The Name from function is $name.");
}
String primeMinisterName() {
  return "Asgar abbas";
}

/*
In this program, primeMinisterName() is the function which has String keyword before function name,
means it return String value, and the empty pair of parentheses suggests that there is no parameter that is passed to the function.
*/
